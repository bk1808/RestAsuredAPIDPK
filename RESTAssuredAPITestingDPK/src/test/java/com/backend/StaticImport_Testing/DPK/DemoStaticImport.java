package com.backend.StaticImport_Testing.DPK;

public class DemoStaticImport {

	/* static import is one of the concept in Java, where you can import static methods
	 * without using a class name whatever static methods are available in the class*/
	
	public static void given() {
		
		System.out.println("==execute given method==");
	}
	
	public static void when() {
		
		System.out.println("==execute when method==");
	}
	
	public static void then() {
		
		System.out.println("==execute then method==");
	}
	
}
