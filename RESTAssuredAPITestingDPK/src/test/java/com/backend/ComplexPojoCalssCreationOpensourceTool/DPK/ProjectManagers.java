package com.backend.ComplexPojoCalssCreationOpensourceTool.DPK;

public class ProjectManagers {

	private String name;
	private String id;
	
		
	public ProjectManagers() {
		super();
	}


	public ProjectManagers(String name, String id) {
		super();
		this.name = name;
		this.id = id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}
		
		
	
}
